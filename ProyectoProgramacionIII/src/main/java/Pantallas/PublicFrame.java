    private void eliminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminaActionPerformed
int filaSeleccionada = this.tablatuits.getSelectedRow();
    if (filaSeleccionada != -1) { 
        String idRegistro = (String) tablatuits.getValueAt(filaSeleccionada, 0);

        try {
            Conexion conexion = new Conexion();
            Connection con = conexion.getConnector();
            String query = "DELETE FROM Tuits3 WHERE contenido = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, idRegistro);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro eliminado exitosamente.");
            con.close();


        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar el registro: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila para eliminar.");
    }    
    }//GEN-LAST:event_eliminaActionPerformed


    
