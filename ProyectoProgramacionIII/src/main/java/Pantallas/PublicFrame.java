    private void actualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizaActionPerformed
DefaultTableModel model1 = (DefaultTableModel) tablatuits.getModel();
 model1.setRowCount(0);
        try {
    
    Conexion conexion = new Conexion();
    Connection con = conexion.getConnector();


    String query = "SELECT contenido FROM Tuits3";
    PreparedStatement pst = con.prepareStatement(query);
    ResultSet rs = pst.executeQuery();

   
    while (rs.next()) {
        String contenido = rs.getString("contenido");
        model1.addRow(new Object[]{contenido});
    }

    con.close();
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Error al obtener los tuits: " + e.getMessage());
}
    }//GEN-LAST:event_actualizaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
               String tuit = TuitText.getText();
        if (tuit.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El tuit no puede estar vacío.");
            return;
        }
        try {
            Conexion conexion = new Conexion();
            Connection con = conexion.getConnector();
            String query = "INSERT INTO Tuits3 (contenido) VALUES (?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, tuit);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Tuit publicado exitosamente.");
            con.close();
                         TuitText.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al publicar el tuit: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

